package basicjava.operator;

public class ComparitiveOperator {
    public static void main(String[] args) {
        //>  <  >=  <=  == !=  are the comparative basicjava.operator if printed give boolean return

        int a=60,b=100;
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a<=b);//first less than then equal to and in < = there is OR basicjava.operator
        System.out.println(a>=b);
        System.out.println(a==b);
        System.out.println(a != b);

    }
}
